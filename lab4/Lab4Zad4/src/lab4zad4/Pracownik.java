package lab4zad4;


public class Pracownik {
    private int age;
    private String gender;
    
    public Pracownik(int age, String gender){
        this.age = age;
        this.gender = gender;
    }
    
    public int getAge() {
        return this.age;
    }
    
    public String getGender() {
        return this.gender;
    }
}
